<h4>How do you manage the regulations and the permit review process?</h4>
<p>Whether the architect uses a checklist or a meeting, he or she should express that they begin researching city codes or meeting with the city’s permitting office at the beginning of the project, not after the design is completed.
</p>

<h4>How will you manage my budget?</h4>
<p>Architects often provide preliminary estimates based on their experience or from area contractors. No matter who tabulates it, don’t move past the schematic phase without this figure. It verifies the project is on track to meet budget, or if the design or budget needs adjustment. </p>

<h4>Do you stay involved during construction?</h4>
<p>An architect should include construction services as a default or at least as an option. If the architect isn’t involved, the construction drawings might get misinterpreted and you could dilute or lose an aesthetic or technical feature. Accordingly you can ask your architect to make a site visit once a week or once every other week, depending on the phase of your project.</p>


<h4>What are your charges?</h4>
<p>Architects usually charge a fixed fee or a percentage of construction costs. As a home owner, you should initially clarify what is and isn’t included in the fees. Also getting a written agreement even for preliminary services would be beneficial.</p>

<h4>What is your hourly rate for additional work?</h4>
<p>Changes to construction drawings, detailed regulatory, or variance permitting are all services that typically fall under an architect’s hourly rate. Before starting the project you need to be clear with all these charges.</p>

<h4>Can you provide me with references?</h4>
<p>Ask your architect about past clients and contractors they’ve worked with, and then follow up on the references they give. They’ll offer insight into how the architect performs during each stage of the project. You should not stop your search with the first contractor you like. It’s always a good idea to get multiple bids, which may give you some bargaining power with the one you ultimately pick.</p>

<h4>What are the important issues, considerations, and challenges of my project?</h4>
<p>Before starting the project, you must get the big picture view from your architect about the project. They’ll guide you with construction, city approvals, and design challenges you may not have been aware of. It’s also worth asking if the architect has previously tackled a similar project to yours</p>

<h4>What’s your role with the contractor?</h4>
<p>Once construction starts, much of the project will be in the hands of your contractor. Many architects recommend contractors they have a good track record with. But you will also have to be clear with your role in dealing with the contractor. 
</p>

<h4>How do you charge?</h4>
<p>Architects usually charge a percentage of the total project cost, anywhere from 5%-20%, depending on the services provided, the complexity of the job, and the renown of the architect. Ask what percentage the architect will charge for your project, and when and how payments will be due.
</p>
<p>Architects typically bill monthly, starting as soon as they begin work. But most up-front design work happens before you bring in a contractor and know the total project cost.</p>

<h4>Can you provide Three-Dimensional Drawings?</h4>
<p>Reading a standard two-dimensional plan isn’t easy. Even if you can tell where the walls, windows, and doors are, you may not get an accurate feel for how the design will look actually. Ask your architect how the ideas and drawings will be presented. Most architects now use software to render 3-D images that can be rotated and viewed from multiple angles.</p>

<h4>What are your credentials?</h4>
<p>Before hiring a designer for your bathroom, you should be aware of his or her credentials. Ask for licenses and certifications that are specifically related to your desired project.</p>

<h4>What does your previous work look like?</h4>
<p>You should go through the portfolio of your designer’s past projects .This will give you an idea of his or her range of abilities and help you visualize the end result of your bathroom renovation.</p>

<h4>What have your clients said about your work?</h4>
<p>You should feel encouraged asking your designer about his previous client’s reviews on his work. Also pay special attention to what customers appreciate about them most.</p>

<h4>How do you charge, and what does that include?</h4>
<p>Before starting the project, it is important to be clear with all the charges. This will help you manage your project within the decided budget, avoiding any useless expenses.</p>

<h4>Can you give me a written estimate?</h4>
<p>Never hesitate asking your designer about a written estimate for your project. This would prevent any future issues in the contract.</p>

<h4> What do your services include?</h4>
<p>There is a separate list of services provided by different designers. Before starting with the project, you should ask your designer about all the services he would be providing.</p>

<h4>What technical training have you received?</h4>
<p>You should enquire about the work experience of your flooring contractor. Also, ask him whether he participates in on-going technical training to keep up with the industry’s latest advancements. Ask if your contractor attends conventions, training classes, and educational opportunities.</p>

<h4>How do you manage dust while flooring?</h4>
<p>It is extremely important to know about the dust removal system that your flooring contractor would use while floor standing. This would prevent you from cleaning fine dust for the next few months after the floor installation.</p>

<h4>What kind of warranties do you offer?</h4>
<p>In case of installation issues, your flooring contractor needs to have a warranty in place for all installations and materials. For example, they may offer a 12 month warranty to cover labour or flooring materials.</p>

<h4>What flooring products do you install?</h4>
<p>You should ask your contractor about the carpeting, laminating, ceramic tile, luxury vinyl, and hardwood flooring. This will help you understand about all the flooring products your contractor installs.</p>

<h4>Will you provide me with floor care instructions?</h4>
<p>Your flooring professional should provide you with expert floor care advice or manufacturer-specific cleaning instructions, or he may also direct you to the proper resources.</p>

<h4> Do You Use Subcontractors?</h4>
<p>Flooring contractors should either have their own trained and experienced employees, or hire only insured subcontractors.</p>

<h4>Are you insured and certified to install my flooring?</h4>
<p>It’s important to be ensured about the certification of your flooring contractor. This will keep you confident that proper guidelines are followed in your project. It is also important to make sure your contractor carries insurance and will assume liability in the case of any unrelated damage they might potentially cause while the work is performed.</p>


<h4>How do I ensure my home has the perfect flooring?</h4>
<p>Establishing the right flooring for your home is not easy. You need to think about maintenance, durability and how the particular flooring will make the room look and feel. The choices are many from concrete, tile and marble to wood and vinyl. Also, flooring material choices are impacted by the space, carpets and rugs are used in the rooms. Don’t forget to ask tiling professional to suggest patterns and designs to enhance the appearance of the flooring.</p>

<h4>Do you offer designer discounts?</h4>
<p>Designers have established relationships with manufacturers, retailers, and other designers. As a result, they often get discounts when they purchase products directly. Enquiring about this can help you save your money.</p>

<h4>Can you show me examples of similar work with my preferred style, room, and budget?</h4>
<p>You should ask your interior designer for his portfolio. Make sure your designer has experience working with the room, styles, and budgets that are required for your project. Also, this will tell you a lot about their personal style, and whether or not it is compatible with the type of style you’re looking for.
</p>

<h4>Explain what is the difference between interior design and interior decoration?</h4>
<p><u>Interior design:</u> Interior designer have technical expertise to know the vast array of options and give creative solutions to the space they use for design</p>
<p><u>Interior decorating:</u> It is about using various forms of colour and accent to make particular space more visually appealing</p>

<h4>Which types of rooms do you usually redecorate?</h4>
<p>Some interior designers have a great deal of experience working with living rooms, while others specialize in kitchens, bathrooms or bedrooms. If you want to redecorate the entire house, you will want to hire a designer with experience in every type of room; otherwise it is better to hire someone who specializes in the specific room you’re interested in redecorating.
</p>

<h4>Can you show me your reviews?</h4>
<p>Your designer’s portfolio will tell you a lot about their personal style, but for additional information you should seek reviews or references from previous clients. Talking to previous clients or reading their reviews will help you determine how your designer measures up.
</p>

<h4>Which style is your specialty?</h4>
<p>Interior design comes in a wide variety of different styles. Some designers specialize in the modern style while others perform more classical work. Before hiring an interior designer, it is important to find out which style you want to use when redecorating your home. Once you have this information, it will be easier to select the right designer.
</p>

<h4>What is your charging procedure?</h4>
<p>Before hiring an interior designer, make sure whether they charge a flat fee or have hourly rates. Accordingly, you also need to clear them with your budget. Many interior designers will ask you to sign a contract. Remember not to sign anything until you’ve discussed the payment and fully understood what and how you will be charged.
</p>

<h4>Can you work within my budget?</h4>
<p>You should be honest with your kitchen designer about your budget. Also ask him if the remodelling kitchen ideas you hope are feasible for that amount. If all this is established before the renovation begins, you can manage everything without having to answer many financial questions.</p>

<h4>Will you be able to maintain my required aesthetic?</h4>
<p>Before hiring your kitchen designer you need to clear him whether you are interested in a vintage look or a more industrial feel. Also tell him if you envision a kitchen with clean lines and grey accents or warm, cosy colours. Enquiring whether your designer will be able to maintain your desired aesthetic or not is very important.
</p>

<h4>Will the renovation impact our health?</h4>
<p>Construction can be difficult for those with health concerns, with dust and debris in the air. Be sure your designer knows your requirements for the environment, and check if there are any health precautions you should take.</p>

<h4>Will you be able to stand on my required deadline?</h4>
<p>You should clearly tell your designer about the time period you want to devote to the project. This will help your designer to plan and prioritize what needs to be done.</p>

<h4>Will I and my family need to live elsewhere during the renovation?</h4>
<p>Enquiring the designer about any other accommodation arrangements required for you will save your time. Usually designers keep a home liveable during a renovation, but if you want to avoid the fray, plan for other living arrangements well in advance.
</p>

<h4>How are you qualified as a kitchen designer?
</h4>
<p>Just because someone claims to be a kitchen designer doesn’t make it so. You need to enquire well about his qualification. Also ask your designer if he has taken any courses in kitchen design and whether he attends industry trade shows and seminars to keep up with the latest kitchen design trends and products.</p>

<h4>Do you own your equipment?</h4>
<p>It is necessary for your lighting professional to have his own equipment as this will give him more control over availability and pricing.</p>

<h4>Do you have insurance? 
</h4>
<p>You should feel encouraged in asking your pro about his insurance and certification. This will ensure you about his authenticity and credibility.</p>

<h4>What range of services do you offer?</h4>
<p>A good lighting designer will generally specialize because the needs of a residential project are different from a commercial project. They should understand lighting and architectural integration. Some will also do lighting controls design to a minor degree, but most will bring in a controls specialist if it is more extensive. Also, your lighting pro should understand the importance of energy efficiency and maintenance of the design.</p>

<h4>How will you collaborate with other members of the team (for example, architects, interior designers, electricians)?</h4>
<p>Collaboration is critical on a construction project. When the lighting designer is part of the team early in the process they should have the ability to help with integrating the lighting needs with the architectural development. You should ensure your lighting designer to provide layout of the lighting to the electrician. Your lighting pro should be able to meet with the electrical contractor and walk through placements, lighting controls, energy requirements, and luminaire selections. </p>

